package com.hienthai.taynhanhhonnao;

/**
 *
 * @author hienx
 */
public class ObjXepHang {
    private String xepHang;
    private String diem;
    private String ngay;

    public ObjXepHang() {
    }

    public ObjXepHang(String xepHang, String diem, String ngay) {
        this.xepHang = xepHang;
        this.diem = diem;
        this.ngay = ngay;
    }

    public String getXepHang() {
        return xepHang;
    }

    public void setXepHang(String xepHang) {
        this.xepHang = xepHang;
    }

    public String getDiem() {
        return diem;
    }

    public void setDiem(String diem) {
        this.diem = diem;
    }

    public String getNgay() {
        return ngay;
    }

    public void setNgay(String ngay) {
        this.ngay = ngay;
    }

    
    
    
    
    
    
}
